int
resolveIfIndex(const std::string& ifName) {
  unsigned int idx = if_nametoindex(ifName.c_str());
  if (idx == 0) {
    XLOGF(ERR, "Failed to resolve ifIndex for {}: {}", ifName, strerror(errno));
    return -1;
  }
  return static_cast<int>(idx);
}

bool
isEui64LinkLocal(const struct in6_addr& addr) {
  return addr.s6_addr[11] == 0xff && addr.s6_addr[12] == 0xfe;
}

std::vector<std::string>
lookupLinkLocalAddrs(const std::string& ifName) {
  struct ifaddrs* ifaddr = nullptr;
  if (getifaddrs(&ifaddr) == -1) {
    XLOGF(ERR, "getifaddrs failed: {}", strerror(errno));
    return {};
  }
  std::vector<std::string> configured;
  std::vector<std::string> autoGenerated;
  for (auto* ifa = ifaddr; ifa != nullptr; ifa = ifa->ifa_next) {
    if (ifa->ifa_addr == nullptr || ifa->ifa_addr->sa_family != AF_INET6) {
      continue;
    }
    if (ifName != ifa->ifa_name) {
      continue;
    }
    auto* sin6 = reinterpret_cast<struct sockaddr_in6*>(ifa->ifa_addr);
    if (!IN6_IS_ADDR_LINKLOCAL(&sin6->sin6_addr)) {
      continue;
    }
    char buf[INET6_ADDRSTRLEN];
    if (inet_ntop(AF_INET6, &sin6->sin6_addr, buf, sizeof(buf))) {
      if (isEui64LinkLocal(sin6->sin6_addr)) {
        autoGenerated.emplace_back(buf);
      } else {
        configured.emplace_back(buf);
      }
    }
  }
  freeifaddrs(ifaddr);
  std::vector<std::string> result;
  result.reserve(configured.size() + autoGenerated.size());
  result.insert(result.end(), configured.begin(), configured.end());
  result.insert(result.end(), autoGenerated.begin(), autoGenerated.end());
  return result;
}

std::string
ipv4FromVlanIfName(const std::string& ifName) {
  auto dotPos = ifName.rfind('.');
  if (dotPos == std::string::npos) {
    return "0.0.0.0";
  }
  try {
    int vlanId = std::stoi(ifName.substr(dotPos + 1));
    return fmt::format("10.{}.{}.1", vlanId / 256, vlanId % 256);
  } catch (const std::exception&) {
    return "0.0.0.0";
  }
}

